        <h1>Framework</h1>

        <div>
            <h2> O que são Frameworks</h2>
            <p>Frameworks são estruturas compostas por um conjunto de códigos genéricos que permite o desenvolvimento de sistemas e aplicações. Um framework funciona como uma espécie de template ou modelo que, quando utilizado, oferece certos artifícios e elementos estruturais básicos para a criação de alguma aplicação ou software</p>
        </div>

        <div>
            <h2> Por que usar um Framework? </h2>
            <p>As funcionalidades de um framework permitem que os desenvolvedores se dediquem às particularidades do projeto sem precisar se preocupar com os elementos básicos da sua estrutura. Por não precisarem começar tudo do zero, os programadores economizam tempo e dinheiro, além de diminuírem os riscos de erro.</p>

            <p>
                Por isso, alguns dos principais motivos pelos quais frameworks são amplamente utilizados no mundo da programação hoje em dia, são:
            </p>

            <ul>
                <li>Otimizar o tempo de desenvolvimento</li>
                <li>Definir e padronizar as melhores práticas de programação</li>
                <li>Oferecer maior segurança</li>
                <li>Evitar códigos duplicados</li>
                <li>Diminuir a ocorrência de bugs</li>
                <li>Gerar maior consistência no processo de desenvolvimento e nas aplicações criadas</li>
                <li>Reduzir as chances de erro no código</li>
                <li>Possibilitar que os desenvolvedores se dediquem aos elementos específicos do projeto</li>
                <li>Simplificar e encurtar a curva de aprendizado do time de desenvolvimento</li>
                <li>Poupar custos</li>
            </ul>
        </div>

        <div>
            <h2>Tipo de Frameworks</h2>
            <p>
                Há diversos tipos de frameworks, e eles podem ser divididos tanto pelas suas aplicações, quanto por seus modos de implementação (front-end, back-end e JavaScript). 
            </p>

            <h3>Frameworks para Aplicações Web</h3>
            <p>Frameworks para aplicações web dão suporte para o desenvolvimento de sistemas online, como sites, APIs, aplicativos e demais recursos disponíveis na internet. </p>

            <ul>
                <li>
                    <strong>Angular</strong>: O Angular foi desenvolvido e é mantido por um time específico da Google. Alguns exemplos de sites e apps que utilizam este framework são: Netflix, PayPal, UpWork e Nike. 
                </li>

                <li>
                    <strong>Django</strong>:É mais utilizado por startups e empresas de tecnologia em projetos interativos, que contam com uma ampla gama de recursos visuais — como jogos, por exemplo. Alguns grandes sites e sistemas desenvolvidos com ajuda do Django são: Spotify, Instagram, YouTube e Pinterest.
                </li>
                <li>
                    <strong>Laravel</strong>: A comunidade do Laravel é grande e ativa, e muitos sites famosos usam esse framework: BBC, 9gag, Pfizer, Barchart, MyRank e World Walking. 
                </li>
            </ul>


            
            
