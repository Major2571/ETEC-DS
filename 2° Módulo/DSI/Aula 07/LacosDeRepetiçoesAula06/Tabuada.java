
package LacosDeRepetiçoesAula06;

public class Tabuada {

    public static void main(String[] args) {

        int i, resultado, numero=10;

        for (i = 1; i <= 10; i++) {

            resultado = numero * i; // i é meu contador
            System.out.println(numero + " * " + i + " = " + resultado);

        }

    }
}
